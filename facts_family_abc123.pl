% База фактов о семье

% Предикаты: родитель(Родитель, Ребенок)
родитель(иван, мария).
родитель(иван, анна).
родитель(мария, дмитрий).
родитель(мария, елена).
родитель(анна, алексей).

% Предикаты: мужчина(Имя)
мужчина(иван).
мужчина(дмитрий).
мужчина(алексей).

% Предикаты: женщина(Имя)
женщина(мария).
женщина(анна).
женщина(елена).

% Правило: бабушка(Бабушка, Внук)
бабушка(X, Z) :- родитель(X, Y), родитель(Y, Z), женщина(X).

% Правило: дедушка(Дедушка, Внук)
дедушка(X, Z) :- родитель(X, Y), родитель(Y, Z), мужчина(X).

% Правило: брат(Имя, Сестра/Брат)
брат(X, Y) :- родитель(Z, X), родитель(Z, Y), мужчина(X), X \= Y.

% Правило: сестра(Имя, Сестра/Брат)
сестра(X, Y) :- родитель(Z, X), родитель(Z, Y), женщина(X), X \= Y.