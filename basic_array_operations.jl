# Basic Array Operations in Julia

# Define an example array
arr = [10, 20, 30, 40, 50]

# Calculate the sum
array_sum = sum(arr)
println("Sum of the array: $array_sum")

# Calculate the mean
array_mean = mean(arr)
println("Mean of the array: $array_mean")

# Calculate the length
array_length = length(arr)
println("Length of the array: $array_length")

# Find the maximum value
array_max = maximum(arr)
println("Maximum value in the array: $array_max")

# Find the minimum value
array_min = minimum(arr)
println("Minimum value in the array: $array_min")