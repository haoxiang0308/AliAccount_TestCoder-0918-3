<?php
// Проверяем, был ли запрос методом POST
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Пример обработки данных формы
    $name = $_POST['name'] ?? 'Неизвестно';
    $email = $_POST['email'] ?? 'Неизвестно';

    // Простая валидация (например, проверка на пустоту)
    if (!empty($name) && !empty($email)) {
        // Формируем строку данных для сохранения
        $data = date('Y-m-d H:i:s') . " - Имя: " . $name . ", Email: " . $email . "\n";

        // Указываем файл для сохранения
        $logFile = 'form_submissions.txt';

        // Открываем файл для дозаписи и записываем данные
        if (file_put_contents($logFile, $data, FILE_APPEND | LOCK_EX)) {
            echo "Данные успешно сохранены.";
        } else {
            echo "Ошибка при сохранении данных.";
        }
    } else {
        echo "Ошибка: Пожалуйста, заполните все обязательные поля (имя и email).";
    }
} else {
    // Если запрос не POST, возможно, это попытка прямого доступа к скрипту
    echo "Доступ запрещен.";
}
?>