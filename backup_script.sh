#!/bin/bash

# Функция для создания резервной копии файла с случайным именем
create_backup() {
    local source_file="$1"

    # Проверка, существует ли исходный файл
    if [[ ! -f "$source_file" ]]; then
        echo "Ошибка: Файл $source_file не существует."
        return 1
    fi

    # Генерация случайного имени для резервной копии в текущей директории
    local backup_name
    backup_name=$(mktemp --suffix=.bak)

    # Копирование файла
    cp "$source_file" "$backup_name"

    # Вывод имени созданной резервной копии
    echo "Резервная копия создана: $backup_name"
}

# Пример вызова функции, если скрипт запускается с аргументом
if [[ $# -eq 1 ]]; then
    create_backup "$1"
else
    echo "Использование: $0 <путь_к_файлу_для_резервного_копирования>"
    echo "Или вызовите функцию create_backup из скрипта."
fi