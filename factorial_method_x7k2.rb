# Метод для вычисления факториала числа
def factorial(n)
  # Проверка на неотрицательное число
  if n < 0
    raise ArgumentError, "Факториал не определен для отрицательных чисел"
  end

  # Факториал 0 и 1 равен 1
  return 1 if n <= 1

  # Вычисление факториала с помощью итерации
  result = 1
  (2..n).each do |i|
    result *= i
  end
  result
end

# Пример использования
puts factorial(5) # Выведет 120