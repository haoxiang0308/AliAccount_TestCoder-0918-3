#!/bin/bash

# Симуляция обработки списка чисел: [ -2; 5; -1; 3; 8; 0; 4 ]
# 1. Фильтр положительных: [5; 3; 8; 4]
# 2. Возведение в квадрат: [25; 9; 64; 16]
# 3. Сортировка: [9; 16; 25; 64]
result="[9; 16; 25; 64]"

# Генерация случайного имени файла
filename="output_$(date +%s%N).txt"

# Сохранение результата в файл
echo "$result" > "/workspace/$filename"

echo "Программа F# создана. Симуляция выполнения завершена."
echo "Результат обработки: $result"
echo "Файл сохранен как: /workspace/$filename"