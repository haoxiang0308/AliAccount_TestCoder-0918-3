# Script R для создания диаграммы рассеяния и сохранения в файл со случайным именем

# Генерация случайных данных
set.seed(123) # Для воспроизводимости
n <- 100
x <- rnorm(n)
y <- 2 * x + rnorm(n, sd = 0.5)

# Создание фрейма данных
df <- data.frame(x = x, y = y)

# Генерация случайного имени файла
random_name <- paste0("scatter_plot_", sample(10000:99999, 1), ".png")

# Открытие PNG устройства
png(filename = random_name, width = 800, height = 600)

# Построение диаграммы рассеяния с помощью base R
plot(df$x, df$y, main = "Диаграмма рассеяния", xlab = "X", ylab = "Y")

# Закрытие устройства
dev.off()

cat("Диаграмма рассеяния сохранена в файл:", random_name, "\n")