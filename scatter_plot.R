# Генерация случайного имени файла
random_name <- paste0("scatter_plot_", sample(100000:999999, 1), ".png")

# Генерация случайных данных
set.seed(123) # Для воспроизводимости
x <- rnorm(100, mean = 50, sd = 10)
y <- 2 * x + rnorm(100, mean = 0, sd = 5)

# Открытие устройства для сохранения изображения
png(random_name)

# Создание диаграммы рассеяния
plot(x, y, 
     main = "Диаграмма рассеяния",
     xlab = "X переменная",
     ylab = "Y переменная",
     pch = 19, 
     col = "blue")

# Добавление линии регрессии
abline(lm(y ~ x), col = "red", lwd = 2)

# Закрытие устройства
dev.off()

# Вывод имени созданного файла
cat("График сохранен в файл:", random_name, "\n")