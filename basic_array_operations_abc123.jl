# basic_array_operations_abc123.jl
#
# This script demonstrates basic array operations in Julia,
# specifically calculating the sum and mean of an array.

# Define an example array
arr = [1, 2, 3, 4, 5]

# Calculate the sum of the array elements
array_sum = sum(arr)

# Calculate the mean of the array elements
array_mean = mean(arr)

# Print the results
println("Array: ", arr)
println("Sum: ", array_sum)
println("Mean: ", array_mean)