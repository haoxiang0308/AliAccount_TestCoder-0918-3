section .text
    global _start

; Функция сложения двух чисел
; Принимает два аргумента: num1 в eax, num2 в ebx
; Возвращает результат в eax
add_numbers:
    add eax, ebx      ; Складываем значения в eax и ebx, результат в eax
    ret               ; Возвращаемся из функции

_start:
    ; Инициализируем регистры начальными значениями для сложения
    mov eax, 5        ; Загружаем первое число (например, 5) в eax
    mov ebx, 3        ; Загружаем второе число (например, 3) в ebx
    call add_numbers  ; Вызываем функцию сложения

    ; Результат сложения (8) теперь находится в eax

    ; Выход из программы
    ; Для простоты используем системный вызов exit (sys_exit)
    ; sys_exit(0)
    mov eax, 1        ; Номер системного вызова sys_exit
    mov ebx, 0        ; Код возврата 0 (успех)
    int 0x80          ; Вызов ядра Linux