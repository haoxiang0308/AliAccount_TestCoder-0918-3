section .text
    global _start

; Функция сложения двух 32-битных целых чисел
; Вход: EAX = первое число, EBX = второе число
; Выход: EAX = результат сложения
add_func:
    add eax, ebx    ; Складываем EBX с EAX, результат в EAX
    ret             ; Возвращаемся из функции

; Точка входа в программу
_start:
    ; Пример использования: сложим 5 и 3
    mov eax, 5      ; Загружаем первое число в EAX
    mov ebx, 3      ; Загружаем второе число в EBX
    call add_func   ; Вызываем функцию сложения

    ; Результат теперь в EAX (должен быть 8)

    ; Выход из программы
    ; Подготовка к системному вызову exit
    mov eax, 1      ; Номер системного вызова sys_exit
    int 0x80        ; Вызов ядра Linux