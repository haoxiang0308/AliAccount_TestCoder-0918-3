// Структура Point для хранения координат
const Point = struct {
    x: f64,
    y: f64,
};

// Пример использования
pub fn main() void {
    const p = Point{ .x = 3.14, .y = 2.71 };
    // Вывод типа не поддерживается напрямую в Zig без дополнительных библиотек
    // Мы просто создадим экземпляр, чтобы проверить корректность определения структуры
    _ = p;
}