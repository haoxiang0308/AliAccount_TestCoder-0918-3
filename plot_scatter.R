# Скрипт R для построения диаграммы рассеяния и сохранения в файл со случайным именем

# Установим seed для воспроизводимости случайного имени
set.seed(Sys.time())

# Сгенерируем случайное имя файла
random_name <- paste0("scatter_plot_", sample(10000:99999, 1), ".png")

# Создадим искусственные данные для диаграммы рассеяния
x <- rnorm(100)  # 100 случайных значений из нормального распределения для оси X
y <- 2 * x + rnorm(100, sd = 0.5)  # Значения Y, зависящие от X с добавлением шума

# Открываем PNG-устройство с случайным именем файла
png(random_name, width = 800, height = 600)

# Построим диаграмму рассеяния
plot(x, y, main = "Диаграмма рассеяния", xlab = "X", ylab = "Y", pch = 19, col = "blue")

# Закроем PNG-устройство, что приведет к сохранению файла
dev.off()

# Выведем имя созданного файла
cat("Диаграмма рассеяния сохранена в файл:", random_name, "\n")