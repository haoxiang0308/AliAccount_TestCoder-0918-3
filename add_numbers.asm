section .text
    global _start

; Функция сложения двух 32-битных целых чисел
; Принимает два аргумента: num1 в EAX, num2 в EBX
; Возвращает результат в EAX
add_func:
    add eax, ebx      ; Сложение содержимого EAX и EBX, результат в EAX
    ret               ; Возврат из функции

; Точка входа в программу
_start:
    ; Загрузка значений для сложения (например, 5 и 3)
    mov eax, 5        ; Передача первого числа в EAX
    mov ebx, 3        ; Передача второго числа в EBX
    call add_func     ; Вызов функции сложения

    ; Выход из программы
    ; Результат сложения (8) будет в EAX
    mov ebx, 0        ; Код возврата 0
    mov eax, 1        ; Системный вызов sys_exit
    int 0x80          ; Вызов ядра Linux