section .text
    global _start

; Функция для сложения двух чисел
; Вход: EAX = первое число, EBX = второе число
; Выход: EAX = результат сложения
add_func:
    add eax, ebx        ; Складываем EAX и EBX, результат в EAX
    ret                 ; Возвращаемся из функции

_start:
    ; Загружаем числа для сложения в регистры EAX и EBX
    mov eax, 5          ; EAX = 5
    mov ebx, 10         ; EBX = 10

    ; Вызываем функцию сложения
    call add_func       ; Результат сложения будет в EAX (5 + 10 = 15)

    ; Выход из программы
    ; Используем системный вызов sys_exit
    mov eax, 1          ; Номер системного вызова sys_exit
    int 0x80            ; Вызов ядра Linux