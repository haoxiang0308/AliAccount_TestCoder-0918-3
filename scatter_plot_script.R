# Генерация случайных данных
set.seed(123) # Для воспроизводимости
x <- rnorm(100, mean = 50, sd = 10)
y <- 2 * x + rnorm(100, mean = 0, sd = 5)

# Создание случайного имени файла
random_name <- paste0("scatter_plot_", sample(10000:99999, 1), ".png")
print(paste("График будет сохранен как:", random_name))

# Открытие устройства для сохранения графика
png(random_name, width = 800, height = 600)

# Построение диаграммы рассеяния
plot(x, y, 
     main = "Диаграмма рассеяния случайных данных",
     xlab = "X значения",
     ylab = "Y значения",
     pch = 19, 
     col = "blue")

# Добавление линии регрессии
abline(lm(y ~ x), col = "red", lwd = 2)

# Закрытие устройства
dev.off()

print("Диаграмма рассеяния успешно создана и сохранена!")