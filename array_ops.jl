# array_ops.jl
# This script demonstrates basic array operations in Julia.

# Create a sample array
arr = [1, 7, 3, 9, 5]

println("Original Array: ", arr)

# Calculate the sum
sum_result = sum(arr)
println("Sum of elements: ", sum_result)

# Calculate the mean
mean_result = mean(arr)
println("Mean of elements: ", mean_result)
