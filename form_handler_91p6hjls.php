<?php
// Простой обработчик формы

// Проверяем, был ли метод POST
if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    // Получаем данные из формы
    $name = $_POST['name'] ?? 'Не указано';
    $email = $_POST['email'] ?? 'Не указано';

    // Простая валидация (например, проверка на пустоту)
    if (empty($name) || empty($email)) {
        echo "Имя и Email обязательны для заполнения.";
    } else {
        // Здесь можно добавить логику сохранения в базу данных или отправки по email
        // Для примера, просто выведем полученные данные
        echo "Данные формы получены успешно!<br>";
        echo "Имя: " . htmlspecialchars($name) . "<br>";
        echo "Email: " . htmlspecialchars($email) . "<br>";
    }
} else {
    // Если метод не POST, выводим сообщение
    http_response_code(405);
    echo "Метод не разрешен. Используйте POST.";
}
?>