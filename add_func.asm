; Функция на ассемблере x86 для сложения двух чисел
; Предполагается соглашение о вызовах cdecl, где аргументы передаются на стек
; Сложение двух 32-битных целых чисел

section .text
global add_numbers

; Функция: add_numbers
; Аргументы (на стеке):
;   [esp + 4]  - первое число (a)
;   [esp + 8]  - второе число (b)
; Возвращает: результат сложения (a + b) в регистре eax
add_numbers:
    push    ebp         ; Сохранить старый указатель на фрейм
    mov     ebp, esp    ; Установить новый указатель на фрейм

    mov     eax, [ebp + 8]  ; Загрузить первое число (b) в eax
    add     eax, [ebp + 4]  ; Прибавить второе число (a) к eax

    pop     ebp         ; Восстановить указатель на фрейм
    ret                 ; Вернуться, результат в eax